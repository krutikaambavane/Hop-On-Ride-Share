CREATE TABLE HOP_ON.PASSENGER (
  PASSENGER_ID INT NOT NULL AUTO_INCREMENT,
  FIRST_NAME VARCHAR(45) NOT NULL,
  LAST_NAME VARCHAR(45) NULL,
  PASSENGER_ADDRESS VARCHAR(100),
  CITY VARCHAR(50),
  ZIP BIGINT,
  PHONE_NO BIGINT NOT NULL,
  EMAIL_ID VARCHAR(50) NOT NULL,
  USERNAME VARCHAR(20) NOT NULL,
  P_PASSWORD VARCHAR(45) NOT NULL,
  PASSENGER_AVG_RATING FLOAT NULL,
  PRIMARY KEY (PASSENGER_ID));


CREATE TABLE HOP_ON.DRIVER ( 
DRIVER_ID INT NOT NULL AUTO_INCREMENT,  
FIRST_NAME VARCHAR(50) NOT NULL, 
LAST_NAME VARCHAR(50), 
PHONE_NO BIGINT NOT NULL, 
DRIVER_ADDRESS VARCHAR(100), 
CITY VARCHAR(50),
ZIP BIGINT,
EMAIL_ID VARCHAR(50) NOT NULL, 
USERNAME VARCHAR(50) NOT NULL,
D_PASSWORD VARCHAR(50) NOT NULL, 
NON_ACCEPTANCE ENUM('ON','OFF')NOT NULL,
DRIVER_AVG_RATING FLOAT, PRIMARY KEY(DRIVER_ID));




CREATE TABLE HOP_ON.CAR(
CAR_ID INT PRIMARY KEY,
DRIVER_ID INT NOT NULL,
REGISTRATION_NO VARCHAR(50) NOT NULL,
MAKE VARCHAR(50) NOT NULL,
CAR_TYPE ENUM('SUV', 'SEDAN', 'HATCHBACK', 'MINIVAN') NOT NULL,
MODEL VARCHAR(50),
COLOR VARCHAR(20),
POLICY_ID VARCHAR(100) NOT NULL,
INSURANCE_VALID_FROM DATE NOT NULL,
INSURANCE_VALID_TO DATE NOT NULL,
SMOG_CHECK ENUM('Y','N'),
FOREIGN KEY(DRIVER_ID) REFERENCES DRIVER(DRIVER_ID));



CREATE TABLE HOP_ON.RIDE(
RIDE_ID INT PRIMARY KEY,
PASSENGER_ID INT NOT NULL,
CAR_ID INT NOT NULL,
RIDE_TYPE ENUM('LUXURY', 'XL' , 'X', 'POOL') NOT NULL,
RIDE_STATUS ENUM('REQUESTED', 'CURRENT', 'COMPLETED', 'CANCELLED') NOT NULL,
RIDE_SOURCE VARCHAR(100) NOT NULL,
RIDE_DESTINATION VARCHAR(100) NOT NULL,
SOURCE_CITY VARCHAR(50) NOT NULL,
DESTINATION_CITY VARCHAR(50) NOT NULL,
START_DATE DATE NOT NULL,
END_DATE DATE NOT NULL,
START_TIME TIME,
END_TIME TIME,
NO_OF_SEATS INT,
ETA TIME,
PICKUP_TIME TIME,
DRIVER_LOCATION VARCHAR(100),
CANCELLATION_REASON ENUM('Driver too far','Changed your mind','Driver asked me to cancel','Passenger did not show up','Driver ETA is too short'),
RIDE_COST FLOAT NOT NULL,
RIDE_TIP FLOAT,
RIDE_TOTAL_COST FLOAT NOT NULL,
FOREIGN KEY (PASSENGER_ID) REFERENCES PASSENGER(PASSENGER_ID),
FOREIGN KEY (CAR_ID) REFERENCES CAR(CAR_ID)
);

CREATE TABLE HOP_ON.RATING(
	RATING_ID INT NOT NULL  PRIMARY KEY ,
	DRIVER_RATING FLOAT ,
    PASSENGER_RATING FLOAT ,
    RIDE_ID INT NOT NULL,
    FOREIGN KEY(RIDE_ID) REFERENCES RIDE(RIDE_ID));


CREATE TABLE HOP_ON.PAYMENT_MODE(
	PAYMENT_MODE_ID INT PRIMARY KEY,
    PASSENGER_ID INT NOT NULL,
    MODE_TYPE ENUM('CARD','PAYPAL') NOT NULL,
    FOREIGN KEY(PASSENGER_ID) REFERENCES PASSENGER(PASSENGER_ID));

   
CREATE TABLE HOP_ON.PAYPAL(
	PAYPAL_ACCOUNT_NO BIGINT NOT NULL  PRIMARY KEY ,
    PAYMENT_MODE_ID INT NOT NULL,
    FOREIGN KEY(PAYMENT_MODE_ID) REFERENCES PAYMENT_MODE(PAYMENT_MODE_ID));
    
CREATE TABLE HOP_ON.CARD(
	CARD_NO NVARCHAR(25) NOT NULL  PRIMARY KEY ,
	CARD_TYPE ENUM('CREDIT','DEBIT') NOT NULL,
    CARDHOLDER_NAME VARCHAR(50) NOT NULL,
    VALID_THROUGH DATE NOT NULL,
    ZIP INT(5) NOT NULL,
    PAYMENT_MODE_ID INT NOT NULL,
    FOREIGN KEY(PAYMENT_MODE_ID) REFERENCES PAYMENT_MODE(PAYMENT_MODE_ID));    

CREATE TABLE HOP_ON.PAYMENT(
	PAYMENT_ID INT PRIMARY KEY,
    	RIDE_ID INT NOT NULL,
	PAYMENT_MODE_ID INT NOT NULL,
    AMOUNT FLOAT NOT NULL,
    FOREIGN KEY(RIDE_ID) REFERENCES RIDE(RIDE_ID),
    FOREIGN KEY(PAYMENT_MODE_ID) REFERENCES PAYMENT_MODE(PAYMENT_MODE_ID));
    

CREATE TABLE HOP_ON.PASSENGER_SUPPORT(
	P_REP_ID INT PRIMARY KEY,
    P_REP_NAME VARCHAR(50) NOT NULL,
	P_REP_ADDRESS VARCHAR(100),
    P_REP_CONTACT BIGINT NOT NULL,
    P_REP_EMAIL VARCHAR(50) NOT NULL,
    P_REP_USERNAME VARCHAR(20) NOT NULL,
    P_REP_PASSWORD VARCHAR(10) NOT NULL);


CREATE TABLE HOP_ON.DRIVER_SUPPORT(
D_REP_ID INT PRIMARY KEY,
    D_REP_NAME VARCHAR(50) NOT NULL,
	D_REP_ADDRESS VARCHAR(100),
    D_REP_CONTACT BIGINT NOT NULL,
    D_REP_EMAIL VARCHAR(50) NOT NULL,
    D_REP_USERNAME VARCHAR(20) NOT NULL,
    D_REP_PASSWORD VARCHAR(10) NOT NULL);


CREATE TABLE HOP_ON.PASSENGER_INCIDENT(
	P_INCIDENT_ID INT NOT NULL  PRIMARY KEY,
P_START_DATE DATE,
    P_END_DATE DATE,
	P_INCIDENT_TYPE ENUM('LOST AND FOUND', 'ACCIDENT', 'UNETHICAL BEHAVIOUR', 'OTHER ISSUES', 'REFUND ISSUES' ) NOT NULL,
    P_ADDL_DESCRIPTION VARCHAR(200),
    P_INCIDENT_STATUS ENUM('NEW', 'OPEN', 'CLOSED', 'REOPEN') NOT NULL,
    PASSENGER_ID INT NOT NULL,
    RIDE_ID INT NOT NULL,
    P_REP_ID INT,
P_COMMENT VARCHAR(200),
    FOREIGN KEY(PASSENGER_ID) REFERENCES PASSENGER(PASSENGER_ID),
    FOREIGN KEY(RIDE_ID) REFERENCES RIDE(RIDE_ID),
    FOREIGN KEY(P_REP_ID) REFERENCES PASSENGER_SUPPORT(P_REP_ID));

    
    

CREATE TABLE HOP_ON.DRIVER_INCIDENT(
	D_INCIDENT_ID INT NOT NULL  PRIMARY KEY,
D_START_DATE DATE,
    D_END_DATE DATE,
	D_INCIDENT_TYPE ENUM('LOST AND FOUND', 'ACCIDENT', 'UNETHICAL BEHAVIOUR', 'OTHER ISSUES' ) NOT NULL,
    D_ADDL_DESCRIPTION VARCHAR(200),
    D_INCIDENT_STATUS ENUM('NEW', 'OPEN', 'CLOSED', 'REOPEN') NOT NULL,
    DRIVER_ID INT NOT NULL,
    RIDE_ID INT NOT NULL,
    D_REP_ID INT,
 D_COMMENT VARCHAR(200),
    FOREIGN KEY(DRIVER_ID) REFERENCES DRIVER(DRIVER_ID),
    FOREIGN KEY(RIDE_ID) REFERENCES RIDE(RIDE_ID),
    FOREIGN KEY(D_REP_ID) REFERENCES DRIVER_SUPPORT(D_REP_ID));

    

CREATE TABLE HOP_ON.PASSENGER_BLACKLIST(
	P_BLACKLIST_ID INT NOT NULL  PRIMARY KEY,
    PASSENGER_ID INT NOT NULL,
    ADDED_BY INT,
    REMOVED_BY INT,
    P_BLACKLISTED_FROM DATE,
    P_BLACKLISTED_TO DATE,
    P_BLACKLISTED_STATUS ENUM('SUSPENDED' , 'BLOCKED', 'UNBLOCKED'),
    FOREIGN KEY(PASSENGER_ID) REFERENCES PASSENGER(PASSENGER_ID),
    FOREIGN KEY(ADDED_BY) REFERENCES DRIVER_SUPPORT(D_REP_ID),
    FOREIGN KEY(REMOVED_BY) REFERENCES DRIVER_SUPPORT(D_REP_ID));
    

CREATE TABLE HOP_ON.DRIVER_BLACKLIST(
	D_BLACKLIST_ID INT NOT NULL  PRIMARY KEY,
    DRIVER_ID INT NOT NULL,
    D_ADDED_BY INT,
    D_REMOVED_BY INT,
    D_BLACKLISTED_FROM DATE,
    D_BLACKLISTED_TO DATE,
    D_BLACKLISTED_STATUS ENUM('SUSPENDED' , 'BLOCKED', 'UNBLOCKED'),
    FOREIGN KEY(DRIVER_ID) REFERENCES DRIVER(DRIVER_ID),
    FOREIGN KEY(D_ADDED_BY) REFERENCES PASSENGER_SUPPORT(P_REP_ID),
    FOREIGN KEY(D_REMOVED_BY) REFERENCES PASSENGER_SUPPORT(P_REP_ID));


